    <script>
        document.getElementById('login-form').addEventListener('submit', async (e) => {
            e.preventDefault();
            
            // Clear previous errors
            document.getElementById('error-message').classList.add('hidden');
            document.getElementById('email-error').classList.add('hidden');
            document.getElementById('password-error').classList.add('hidden');
            
            const formData = {
                email: document.getElementById('email').value,
                password: document.getElementById('password').value,
                remember: document.getElementById('remember').checked
            };

            try {
                const response = await fetch('/api/login', {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json',
                        'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').content
                    },
                    body: JSON.stringify(formData)
                });

                const data = await response.json();

                if (response.ok) {
                    // Redirect to portfolio dashboard
                    window.location.href = '/portfolio';
                } else {
                    // Show error message
                    document.getElementById('error-text').textContent = data.message || 'Login failed. Please try again.';
                    document.getElementById('error-message').classList.remove('hidden');
                }
            } catch (error) {
                console.error('Login error:', error);
                document.getElementById('error-text').textContent = 'An error occurred. Please try again.';
                document.getElementById('error-message').classList.remove('hidden');
            }
        });
    </script>

